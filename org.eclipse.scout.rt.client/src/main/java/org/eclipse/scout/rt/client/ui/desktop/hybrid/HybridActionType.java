import java.lang.annotation.Target;

/**
 * Define the eventType of the {@link IHybridAction}
 */
@Inherited
@Retention(RetentionPolicy.RUNTIME)
@Target(ElementType.TYPE)
public @interface HybridActionType {
  String value();
}
